ALTER TABLE companies ADD COLUMN IF NOT EXISTS linkedin_company_id text;
ALTER TABLE companies ADD COLUMN IF NOT EXISTS linkedin_url text;
ALTER TABLE companies ADD COLUMN IF NOT EXISTS universal_name text;
ALTER TABLE companies ADD COLUMN IF NOT EXISTS domain text;
ALTER TABLE companies ADD COLUMN IF NOT EXISTS logo_url text;

COMMENT ON COLUMN companies.linkedin_company_id IS 'Stable LinkedIn numeric company id (from experience[].companyId) — primary join key for scraped data';
COMMENT ON COLUMN companies.universal_name IS 'LinkedIn company slug (e.g., "google")';

CREATE UNIQUE INDEX IF NOT EXISTS companies_linkedin_company_id_idx
  ON companies (linkedin_company_id) WHERE linkedin_company_id IS NOT NULL;

-- companies had SELECT + INSERT policies only. Without UPDATE, claiming an
-- existing name-only row by backfilling linkedin_company_id silently affects
-- 0 rows under RLS (the import path runs on a user-token client).
DROP POLICY IF EXISTS "companies_update_authenticated" ON companies;
CREATE POLICY "companies_update_authenticated" ON companies
  FOR UPDATE USING (auth.uid() IS NOT NULL);

-- ═══════════════════════════════════════════════════════════
-- 2. company_locations — auto-managed office registry
-- ═══════════════════════════════════════════════════════════
CREATE TABLE IF NOT EXISTS company_locations (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id int NOT NULL REFERENCES companies(id) ON DELETE CASCADE,
  location_id int NOT NULL REFERENCES locations(id),
  source text NOT NULL DEFAULT 'scraped' CHECK (source IN ('scraped', 'manual')),
  created_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE (company_id, location_id)
);

COMMENT ON TABLE company_locations IS 'Known office locations per company. Rows established automatically by import rule 1 (experience-level locations) or seeded manually; anchor for location-scoped recruiting intel.';

ALTER TABLE company_locations ENABLE ROW LEVEL SECURITY;

-- Shared lookup data (mirrors locations): readable + insertable by any
-- authenticated user; deletable for phantom-office correction. No UPDATE
-- policy — the importer inserts with ON CONFLICT DO NOTHING.
CREATE POLICY "company_locations_select" ON company_locations
  FOR SELECT TO authenticated USING (true);

CREATE POLICY "company_locations_insert" ON company_locations
  FOR INSERT TO authenticated WITH CHECK (true);

CREATE POLICY "company_locations_delete" ON company_locations
  FOR DELETE TO authenticated USING (true);

