-- ═══════════════════════════════════════════════════════════
-- 5. target_companies — the recruiting layer (user-scoped)
-- ═══════════════════════════════════════════════════════════
CREATE TABLE IF NOT EXISTS target_companies (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id uuid NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  company_id int NOT NULL REFERENCES companies(id) ON DELETE CASCADE,
  priority_score numeric,
  tier text,
  program_name text,
  app_window_text text,
  next_app_date date,
  status text NOT NULL DEFAULT 'researching' CHECK (status IN ('researching', 'outreach_active', 'applied', 'interviewing', 'closed')),
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE (user_id, company_id)
);

COMMENT ON COLUMN target_companies.tier IS 'Segment/geo label from the target sheet (e.g. "Utah/Silicon Slopes", "Big Tech")';
COMMENT ON COLUMN target_companies.app_window_text IS 'Free-text application-window hint imported from the sheet — display only, never sorted or parsed';
COMMENT ON COLUMN target_companies.next_app_date IS 'Real application date set by hand when learned; the only field sorting/alerts use';

ALTER TABLE target_companies ENABLE ROW LEVEL SECURITY;

CREATE POLICY "target_companies_select_own" ON target_companies
  FOR SELECT USING (user_id = auth.uid());

CREATE POLICY "target_companies_insert_own" ON target_companies
  FOR INSERT WITH CHECK (user_id = auth.uid());

CREATE POLICY "target_companies_update_own" ON target_companies
  FOR UPDATE USING (user_id = auth.uid());

CREATE POLICY "target_companies_delete_own" ON target_companies
  FOR DELETE USING (user_id = auth.uid());

