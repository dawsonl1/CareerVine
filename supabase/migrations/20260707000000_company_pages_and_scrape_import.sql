-- 8. suppressed_imports — deletion tombstones
-- ═══════════════════════════════════════════════════════════
CREATE TABLE IF NOT EXISTS suppressed_imports (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id uuid NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  linkedin_url text NOT NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE (user_id, linkedin_url)
);

COMMENT ON TABLE suppressed_imports IS 'Tombstones written when an imported contact is deleted; bulk import skips these so deleted junk does not resurrect on the next tranche. linkedin_url is stored in canonical form.';

ALTER TABLE suppressed_imports ENABLE ROW LEVEL SECURITY;

CREATE POLICY "suppressed_imports_select_own" ON suppressed_imports
  FOR SELECT USING (user_id = auth.uid());

CREATE POLICY "suppressed_imports_insert_own" ON suppressed_imports
  FOR INSERT WITH CHECK (user_id = auth.uid());

CREATE POLICY "suppressed_imports_delete_own" ON suppressed_imports
  FOR DELETE USING (user_id = auth.uid());
