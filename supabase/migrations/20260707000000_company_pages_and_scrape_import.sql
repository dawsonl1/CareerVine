-- contact_companies.location was added by
-- careervine/supabase/migrations/20250217_add_location_to_contact_companies.sql
-- (commit 9bec200) and deleted in 9c718fe without being re-created when
-- migrations moved to the repo root. The column exists in production (the
-- manual add-company UI writes it on every insert); this re-tracks it so
-- fresh environments match production. No-op on production.
ALTER TABLE contact_companies ADD COLUMN IF NOT EXISTS location TEXT;

COMMENT ON COLUMN contact_companies.location IS 'Legacy free-text job location from manual entry (e.g., "San Francisco, CA"). Scraped imports use location_id/location_raw instead.';

